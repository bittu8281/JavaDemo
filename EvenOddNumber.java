package ProgrammingCode;

public class EvenOddNumber {

	public static void main(String[] args) {
		 int number = 25;
		
				 for(int n=1;n<=number;n++){
			    if(n%2==0){
			       System.out.println("This is even number = " + n);
			    }
			    else{
			        System.out.println( "This is odd number = " + n);
			    }
			 }
	}

}
